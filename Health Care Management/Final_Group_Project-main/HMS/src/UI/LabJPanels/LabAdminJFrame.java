    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
  
 try {
    String procedureName = testNameField.getText();
    float price = Float.parseFloat(testPriceField.getText());
     if(procedureName.equals("")||price == 0.0 )
     {
         JOptionPane.showMessageDialog(this, "Please check data and try again!");
     }
    // Retrieve the selected test from the table
     else{
    int selectedRow = TestsTable.getSelectedRow();
    if (selectedRow >= 0) {
        LabTests test = (LabTests) testsTableModel.getValueAt(selectedRow, 0);

        // Update the test's properties
        test.setProcedureName(procedureName);
        test.setPrice(price);

        // Refresh the table and display a success message
        displayTests();
        JOptionPane.showMessageDialog(this, "Test updated");
    } else {
        JOptionPane.showMessageDialog(null, "Select a Row");
    }
     }
} 
 catch (HeadlessException | NumberFormatException ex) {
    JOptionPane.showMessageDialog(this, "Please check data and try again!");
}
  testNameField.setText("");
         testPriceField.setText("");
    }//GEN-LAST:event_updateActionPerformed

