 public void displayTests()
    {
        ArrayList<LabTests> tests = this.business.getLabTestsList().getLabTests();
        
        if(tests.size() >= 0) {
            testsTableModel.setRowCount(0);
            for(LabTests t: tests) {
                Object row[] = new Object[2];
                row[0] = t;
                row[1] = t.getPrice();
                
                
                testsTableModel.addRow(row);
            }
        }
    }

