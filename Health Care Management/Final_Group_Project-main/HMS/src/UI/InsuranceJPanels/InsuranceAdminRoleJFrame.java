    public void displayInsurancePlans() {
        ArrayList<InsuranceEnrollment> ips = this.business.getInsuranceEnrollmentRequests().getEnrollments();
        if (ips.size() >= 0) {
            insPlansTableModel.setRowCount(0);
            for (InsuranceEnrollment h : ips) {
                Object row[] = new Object[5];
                row[0] = h.getEnrollmentId();
                row[1] = h.getPatient().getId();
                row[2] = h.getPatient().getFirstName() + ' ' + h.getPatient().getLastName();
                row[3] = h.getInsurancePlan().getPlanName();
                row[4] = h.getStatus();
                insPlansTableModel.addRow(row);
            }
        }
    }

    public void polulateApplicantTable() {
        ArrayList<InsuranceAgent> ips = this.business.getInsuranceAgents().getInsuranceAgents();
        if (ips.size() >= 0) {
            ApplicantModel.setRowCount(0);

            for (InsuranceAgent h : this.business.getInsuranceAgents().getInsuranceAgents()) {
                Object row[] = new Object[6];
                row[0] = h.getId();
                row[1] = h.getFirstName();
                row[2] = h.getLastName();
                row[3] = h.getEmail();
                row[4] = h.getPhoneNumber();
                row[5] = h.getAddress();
                

                ApplicantModel.addRow(row);
            }
        }

    }

