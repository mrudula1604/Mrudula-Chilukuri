    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
        // TODO add your handling code here:
     try{
        int selectedRow = applicantDetailsTable.getSelectedRow();
        if (selectedRow >= 0) {
            String agentId = applicantDetailsTable.getValueAt(selectedRow, 0).toString();
            InsuranceAgent agent = this.business.getInsuranceAgents().findInsuranceAgent(agentId);

            // Display the agent details in a dialog box
            JTextField firstNameField = new JTextField(agent.getFirstName());
            JTextField lastNameField = new JTextField(agent.getLastName());
            JTextField emailField = new JTextField(agent.getEmail());
            JTextField phoneField = new JTextField(agent.getPhoneNumber());
            JTextField addressField = new JTextField(agent.getAddress());

            JPanel panel = new JPanel(new GridLayout(0, 1));
            panel.add(new JLabel("First Name:"));
            panel.add(firstNameField);
            panel.add(new JLabel("Last Name:"));
            panel.add(lastNameField);
            panel.add(new JLabel("Email:"));
            panel.add(emailField);
            panel.add(new JLabel("Phone Number:"));
            panel.add(phoneField);
            panel.add(new JLabel("Address:"));
            panel.add(addressField);

            int result = JOptionPane.showConfirmDialog(null, panel, "Update Agent Details", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
            
            if (result == JOptionPane.OK_OPTION) {
                // Validate the inputs
                if (firstNameField.getText().isEmpty() || lastNameField.getText().isEmpty() || emailField.getText().isEmpty() || phoneField.getText().isEmpty() || addressField.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Please enter values for all fields", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
            
                // Update the agent details
                agent.setFirstName(firstNameField.getText());
                agent.setLastName(lastNameField.getText());
                agent.setEmail(emailField.getText());
                agent.setPhoneNumber(phoneField.getText());
                agent.setAddress(addressField.getText());

                // Update the table
                ApplicantModel.setValueAt(agent.getFirstName(), selectedRow, 1);
                ApplicantModel.setValueAt(agent.getLastName(), selectedRow, 2);
                ApplicantModel.setValueAt(agent.getEmail(), selectedRow, 3);
                ApplicantModel.setValueAt(agent.getPhoneNumber(), selectedRow, 4);
                ApplicantModel.setValueAt(agent.getAddress(), selectedRow, 5);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Select a Row");
        }
        }
        } catch (Exception ex) {
    ex.printStackTrace();
    JOptionPane.showMessageDialog(null, "Error occurred: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}


    }//GEN-LAST:event_UpdateBtnActionPerformed

