    private void recordVitalActionPerformed(java.awt.event.ActionEvent evt) {                                            

        try {
            String patientId = selPatientCBox.getSelectedItem().toString().split(";")[0];

            Patient p = this.business.getPatients().findPatient(patientId);

            String bp = bpTextField.getText();
            int hr = Integer.parseInt(hrTextField.getText());
            Float weight = Float.valueOf(weightTextField.getText());
            Float height = Float.valueOf(heightTextField.getText());
            if (bpTextField.getText().equals("") || hrTextField.getText().equals("") || weightTextField.getText().equals("") || heightTextField.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Please check data and try again!");
            } else {
                p.getVitals().addVital(p, bp, hr, weight, height);
                displayVitals(patientId);
            }

        } catch (HeadlessException | NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please check data and try again!");
        }
        bpTextField.setText("");

        hrTextField.setText("");
        weightTextField.setText("");
        heightTextField.setText("");

    }                                            


