    private void addDocBtnActionPerformed(java.awt.event.ActionEvent evt) {                                          

        try {
            String firstName = docFNameTextField.getText();
            String lastName = docLNameTextField.getText();
            String phone = docPhoneTextFIeld.getText();
            String email = docEmailTextField.getText();
            String address = docAddressTextField.getText();
            String degree = docDegreeTextField.getText();
            String speciality = specialityCBox.getSelectedItem().toString();
            Float fee = Float.valueOf(docFeeTextField.getText());
            String userName = unameTextField.getText();
            String password = passTextField.getText();

            String hospName = hospCBox.getSelectedItem().toString();

            if (this.business.getTopLevelUserAccountDirectory().checkIfUsernameIsAlreadyTaken(userName)) {
                JOptionPane.showMessageDialog(null, "Username is already taken! please enter a unique username");
                return;
            }

            //validations
            if (Validations.ValidateEmail(email) && Validations.ValidatePhone(phone)) {

                Hospital hospital = this.business.getHospitals().findHospitalByName(hospName);
                DoctorDirectory docs = hospital.getDoctors();
                Doctor d = docs.addDoctor(hospital,
                        firstName,
                        lastName,
                        email,
                        phone,
                        address,
                        degree,
                        speciality,
                        userName,
                        password,
                        fee);

                displayDoctors();

                //create a useraccount and add it to the top level dir
                UserAccount ua = new UserAccount();
                ua.setUsername(userName);
                ua.setPassword(password);
                ua.setRole(new DoctorRole());
                ua.setPersonId(d.getId());
                //staff.setUserAcc(ua);

                this.business.getTopLevelUserAccountDirectory().getUseraccountList().add(ua);
                JOptionPane.showMessageDialog(null, "Doctor registered successfully!");
            } else {
                JOptionPane.showMessageDialog(null, "Please check the email and phone number fields"
                        + " and enter valid values");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please check the data and enter valid values");
        }
        docFNameTextField.setText("");
        docAddressTextField.setText("");
        docDegreeTextField.setText("");
        docEmailTextField.setText("");
        docFeeTextField.setText("");
        docLNameTextField.setText("");
        docPhoneTextFIeld.setText("");
    }                                           


