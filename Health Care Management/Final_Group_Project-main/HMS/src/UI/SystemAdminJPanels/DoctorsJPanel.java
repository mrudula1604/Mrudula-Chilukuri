    private void populateHospitals() {
        for (Hospital b : this.business.getHospitals().getHospitals()) {
            hospCBox.addItem(b.getHospitalName());
        }
    }

    public void displayDoctors() {
        ArrayList<Hospital> hospitals = this.business.getHospitals().getHospitals();

        if (hospitals.size() >= 0) {
            doctorsTableModel.setRowCount(0);
            for (Hospital h : hospitals) {
                for (Doctor d : h.getDoctors().getDoctorsList()) {
                    Object row[] = new Object[6];
                    row[0] = d.getId();
                    row[1] = d.getFirstName() + ' ' + d.getLastName();
                    row[2] = d.getPhoneNumber();
                    row[3] = d.getSpeciality();
                    row[4] = d.getConsulatationFee();
                    row[5] = h.getHospitalName();
                    doctorsTableModel.addRow(row);
                }
            }
        }

    }

