    private void approveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveBtnActionPerformed
        //String studentId = useraccount.getPersonId();
        //Student currentStudent = this.business.getStudentDir().findStudent(studentId);
        int selectedRowIndex = studentCreditsTable.getSelectedRow();
        String trId = studentCreditsTable.getValueAt(selectedRowIndex, 0).toString();
        int totalCredits = Integer.parseInt(studentCreditsTable.getValueAt(selectedRowIndex, 3).toString());
        var currentTR = this.business.getTranscriptRequests().findTranscriptRequest(trId);
        
        if (totalCredits < 8 || currentTR.getStatus().equals("declined"))
        {
            JOptionPane.showMessageDialog(null, "Student hasnt completed the mandatory(8) credits");
        }
        else
        {
            
            currentTR.setStatus("approved");
        }
        
        displayRequests();
    }//GEN-LAST:event_approveBtnActionPerformed

