    public void displayRequests()
    {
        ArrayList<TranscriptRequest> transRequests = this.business.getTranscriptRequests().getTranscriptRequests();
        
        if(transRequests.size() >= 0) {
            studentsTableModel.setRowCount(0);
            for(TranscriptRequest c: transRequests) {
                var student = c.getStudent();
                var courses = student.getRegisteredCourses().getCourseList();
                int totalCredits = 0;
                for (Course cr : courses)
                {
                    totalCredits = totalCredits + cr.getCredits();
                }
                Object row[] = new Object[5];
                row[0] = c.getTrId();
                row[1] = student.getStudentId();
                row[2] = student.getName();
                row[3] = totalCredits;
                row[4] = c.getStatus();
                
               studentsTableModel.addRow(row);
            }
        }
    }

